const reactRoot = document.getElementById("react-root");
type CapturedUser = User & { status: "done" };
type CapturingUser = User & { status: "capturing" };
type ErrordCapture = { status: "failed" };
type CaptureStatus = CapturedUser | CapturingUser | ErrordCapture;
const users = new Map<string, CaptureStatus>();

capturedAlready().forEach(async (username) => {
  users.set("@" + username, {
    status: "done",
    displayName: "",
    bio: "",
    username: "@" + username,
    image: "",
  });
});

const observer = new MutationObserver(
  debounce(() => {
    const list = document.querySelector('[aria-label="Timeline: Following"]');
    if (!list) return;

    // prettier-ignore
    const nodes = list.querySelectorAll<HTMLDivElement>('[data-testid="UserCell"]');
    // -------------------------------- â†“ debug purposes
    const user_cells = Array.from(nodes).slice(0, 1);

    for (const element of user_cells) {
      (async () => {
        const username = getUserName(element);
        try {
          if (!username) return;

          const prior = users.get(username);

          if (prior && prior.status === "done") {
            highlighter(element, "green");
            return;
          }

          highlighter(element, "orange");
          if (prior && prior.status === "capturing") {
            console.log("Already capturing", username);
            return;
          } else {
            console.log("Capturing", username);
          }
          const user = await captureUser(element);
          console.log(user);
          users.set(username, { status: "capturing", ...user });
          const err = await chrome.runtime.sendMessage({
            type: "user",
            ...user,
          });
          if (err) throw err;
          users.set(username, { status: "done", ...user });
          highlighter(element, "green");
        } catch (error) {
          console.error(username, error?.message || error);
          users.set(username, { status: "failed" });
          highlighter(element, "red");
        }
      })();
    }
  }, 500)
);

observer.observe(reactRoot, {
  subtree: true,
  childList: true,
  attributes: false,
});

function highlighter(node: HTMLDivElement, color: string) {
  node.style.boxShadow = `10px 0 ${color}, -10px 0 ${color}`;
}

function getUserName(node: HTMLDivElement) {
  const links = node.querySelectorAll("a");
  return links.item(2)?.textContent;
}

async function captureUser(node: HTMLDivElement) {
  const username = getUserName(node);

  const links = node.querySelectorAll("a");
  const displayName = crawlTextWithEmoji(links.item(1));
  const bio =
    crawlTextWithEmoji(
      // prettier-ignore
      node
        ?.childNodes.item(0)
        ?.childNodes.item(1)
        ?.childNodes.item(1) as HTMLElement
    ) || "<Unable to grab bio>";

  const img = node.querySelector<HTMLImageElement>(
    '[data-testid^="UserAvatar-Container"] img'
  );

  const user: User = { username, displayName, bio, image: img.src };

  return user;
}

function crawlTextWithEmoji(el: HTMLElement) {
  let text = "";
  const nodes = [el] as Node[];
  while (nodes.length > 0) {
    const node = nodes.shift();
    if (node === null) {
      continue;
    } else if (node.nodeType === Node.TEXT_NODE) {
      text = node.textContent + text;
    } else if (
      node.nodeType === Node.ELEMENT_NODE &&
      (node as Element).tagName.toLowerCase() === "img"
    ) {
      const emoji = (node as HTMLImageElement).getAttribute("alt");
      text = emoji + text;
    } else if (node.childNodes.length > 0) {
      node.childNodes.forEach((child) => nodes.unshift(child));
    }
  }

  return text;
}

function debounce(func, timeout = 300) {
  let timer;
  return (...args) => {
    clearTimeout(timer);
    timer = setTimeout(() => {
      func.apply(this, args);
    }, timeout);
  };
}

